public class MathProblemSolver {
    public static void main(String[] args) {
        // Example 1: Add two numbers
        System.out.println("Adding 3 and 5:");
        int result = 3 + 5;
        System.out.println(result);

        // Example 2: Subtract two numbers
        System.out.println("Subtracting 7 from 9:");
        int difference = 9 - 7;
        System.out.println(difference);

        // Example 3: Multiply two numbers
        System.out.println("Multiplying 4 by 6:");
        int product = 4 * 6;
        System.out.println(product);

        // Example 4: Divide two numbers
        System.out.println("Dividing 8 by 2:");
        int quotient = 8 / 2;
        System.out.println(quotient);
    }
}
