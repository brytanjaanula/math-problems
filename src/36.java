import java.util.*;

public class MathProblems {
    public static void main(String[] args) {
        // Example problem: Summing numbers from 1 to 50
        int sum = 0;
        for (int i = 1; i <= 50; i++) {
            sum += i;
        }
        System.out.println("The sum of numbers from 1 to 50 is: " + sum);
    }
}
