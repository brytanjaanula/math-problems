public class MathProblems {
    public static void main(String[] args) {
        // Example: Addition
        int result = 5 + 3;
        System.out.println("The sum of 5 and 3 is " + result);

        // Example: Subtraction
        int difference = 8 - 2;
        System.out.println("The difference between 8 and 2 is " + difference);

        // Example: Multiplication
        int product = 7 * 6;
        System.out.println("The product of 7 and 6 is " + product);

        // Example: Division
        int quotient = 10 / 3; // Equivalent to 10 divided by 3
        System.out.println("The division result of 10 and 3 is " + quotient);
    }
}
